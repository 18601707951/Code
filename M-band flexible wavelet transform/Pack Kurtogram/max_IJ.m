function [I,J,M] = max_IJ(X)
% [I,J] = max_IJ(X) returns the row and column indices of
% the maximum in the matrix X.
%
% -------------------
% J. Antoni : 07/2004
% -------------------

[temp,tempI] = max(X);%求每列的最大值，并记录每列最大值的行位置
[M,J] = max(temp);%求矩阵最大值，并记录列位置
I = tempI(J);%矩阵最大值的行位置

