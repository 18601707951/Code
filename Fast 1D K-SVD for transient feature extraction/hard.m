%迭代硬阈值算法，若解决的是argmin ||X-b||_{2}^2+\lambda||X||_{0}这一问题。在此处需调用hard(b,sqrt(lamda))
%若需要解决的是argmin 1/2||X-b||_{2}^2+\lambda||X||_{0}，则需调用hard(b,sqrt(2*lamda))
% =========================================================================
%                          Written by Yi Qin
% =========================================================================

function [ x ] = hard( b,T )  
    sel = (abs(b)>T);  
    x = b.*sel;  
end