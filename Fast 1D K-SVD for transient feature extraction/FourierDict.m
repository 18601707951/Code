function D1=FourierDict(N,l)
% =========================================================================
%                          Written by Yi Qin
% =========================================================================
D1=zeros(N,l*N);
t1 = 0:N-1;
for i = 0:l*N/2
    D1(:,i+1) = cos(2*pi*i/(l*N)*t1);       %(不要引入采样频率)
end
for i = 1:(l*N/2-1)
    D1(:,1+l*N/2+i)=sin(2*pi*i/(l*N)*t1);   %(不要引入采样频率)
end
for j=1:l*N
    D1(:,j) = D1(:,j)/norm(D1(:,j));
end
